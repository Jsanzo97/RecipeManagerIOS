import Foundation
import UIKit

/**
 Colors
 
 This struct contains all the colors used in the app. Conversion from HEX value to UIColor made with http://uicolor.xyz/#/hex-to-ui
 */
struct Colors {
    
    /// HEX #ffffff
    static let white = UIColor.white
    /// HEX #000000
    static let black = UIColor.black
    /// HEX #009de0
    static let mainBlue = UIColor(red: 0.00, green: 0.62, blue: 0.88, alpha: 1.00)
    /// HEX #f2f2f2
    static let lightGrey = UIColor(red:0.95, green:0.95, blue:0.95, alpha:1.0)
    /// HEX #707070
    static let darkGrey = UIColor(red:0.44, green:0.44, blue:0.44, alpha:1.0)
}
